    private void actualizarLista(Vector v) {
        DefaultListModel m = new DefaultListModel();
        if(v  != null){
            for(int i=0; i<v.size(); i++){
                try {
                    String usr = ((InterfazCliente)v.get(i)).getNombre();
                    m.addElement(usr);
                } catch (RemoteException ex) {
                    ex.printStackTrace();
                }
            }
        }
        jList1.setModel(m);
    }
    
