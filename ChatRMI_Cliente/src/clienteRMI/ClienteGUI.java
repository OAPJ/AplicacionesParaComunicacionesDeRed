    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jButton1.getText().equals("Conectar")){
            if(jTextField1.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Debes de poner un usuario");
                return;
            }
            if(jTextField2.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Debes de poner la IP del servidor");
                return;
            }
            try {
                c = new Cliente(jTextField1.getText());
                c.setC(this);
                Registry r = LocateRegistry.getRegistry(jTextField2.getText());
                s = (InterfazServidor)r.lookup("servidor");
                s.registrar(c);
                actualizarLista(s.obtenerClientesActivos());
            } catch (RemoteException ex) {
                ex.printStackTrace();
            } catch (NotBoundException ex) {
                ex.printStackTrace();
            }
            
            jTextField1.setEditable(false);
            jTextField2.setEditable(false);
            jTextArea1.setEditable(true);
            jButton2.setEnabled(true);
            jTextField3.setEditable(true);
            jButton1.setText("Desconectar");
        }
        else{
            actualizarLista(null);
            jTextField1.setEditable(true);
            jTextField2.setEditable(true);
            jTextArea1.setEditable(false);
            jButton2.setEnabled(false);
            jTextField3.setEditable(false);
            jButton1.setText("Conectar");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

